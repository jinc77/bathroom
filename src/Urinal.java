/* Bathroom subclass Urinal that can store information about
 * the bathroom fixture: what number urinal,
 * whether it's occupied, how many times it was used,
 * and who is currently using the urinal.
 * @author Uijin Cho
 */

public class Urinal extends Bathroom {
    /* Constructor to create a Urinal object
     * @param id           the fixture number
     */
    public Urinal(String id) {
        super(id);
    }
}
